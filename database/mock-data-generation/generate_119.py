def compose_comment(parent_cat_id: str) -> str:
    """Tier 1: Assemble a comment from grammar slots."""
    grammar = COMMENT_GRAMMAR.get(parent_cat_id, COMMENT_GRAMMAR["0.0.0.0.0"])
    parts = [
        random.choice(grammar["greeting"]),
        random.choice(grammar["action"]),
        random.choice(grammar["timing"]),
        random.choice(grammar["followup"]),
    ]
    return " ".join(p for p in parts if p).strip()


def compose_feedback(rating: int, parent_cat_id: str = "0.0.0.0.0") -> str:
    """Tier 1: Assemble feedback text from grammar slots + category context."""
    grammar = FEEDBACK_GRAMMAR.get(rating, FEEDBACK_GRAMMAR[3])
    parts = [
        random.choice(grammar["opener"]),
        random.choice(grammar["detail"]),
        random.choice(grammar["recommend"]),
    ]
    base = " ".join(p for p in parts if p).strip()
    # 40% chance: prepend category-specific context
    if random.random() < 0.40:
        ctx_pool = FEEDBACK_CATEGORY_CONTEXT.get(
            parent_cat_id, FEEDBACK_CATEGORY_CONTEXT["0.0.0.0.0"]
        )
        ctx = random.choice(ctx_pool)
        base = f"{ctx} was great. {base}" if rating >= 4 else f"{ctx} fell short. {base}"
    return base


def pick_comment(parent_cat_id: str, leaf_cat_id: str) -> str:
    """Generate a comment using Tier 1 (composition) + Tier 2 (perturbation)."""
    base = compose_comment(parent_cat_id)
    # 30% chance: append subcategory context
    if random.random() < 0.3 and leaf_cat_id != "0.0.0.0.0":
        cat_name = HELP_CATEGORIES.get(leaf_cat_id, leaf_cat_id)
        readable = cat_name.replace("_", " ").title()
        base += f" (Re: {readable})"
    return perturb(base)


def pick_feedback(rating: int, parent_cat_id: str = "0.0.0.0.0") -> str:
    """Generate feedback using Tier 1 (composition) + Tier 2 (perturbation)."""
    return perturb(compose_feedback(rating, parent_cat_id))


