def random_timestamp(start: datetime, end: datetime) -> str:
    if start >= end:
        return start.strftime("%Y-%m-%d %H:%M:%S")
    delta = end - start
    random_seconds = random.randint(0, int(delta.total_seconds()))
    return (start + timedelta(seconds=random_seconds)).strftime("%Y-%m-%d %H:%M:%S")


def make_user_id() -> str:
    return f"cognito_{uuid.UUID(int=random.getrandbits(128), version=4)}"


def make_request_id() -> str:
    return f"REQ-{str(uuid.UUID(int=random.getrandbits(128), version=4))[:8].upper()}"


