def main():
    parser = argparse.ArgumentParser(
        description="Generate synthetic CSVs for request_comments and volunteer_rating"
    )
    parser.add_argument("--rows",        type=int, default=100,
                        help="Rows per table (default: 100)")
    parser.add_argument("--pool-size",   type=int, default=None,
                        help="Internal pool size override")
    parser.add_argument("--seed",        type=int, default=42,
                        help="Random seed (default: 42)")
    parser.add_argument("--users-csv",   type=str, default=None,
                        help="Path to users.csv from another team (for real FK integrity)")
    parser.add_argument("--requests-csv", type=str, default=None,
                        help="Path to request.csv from another team (for real FK integrity)")
    args = parser.parse_args()

    random.seed(args.seed)
    n_rows = args.rows

    print(f"\nSaayam Synthetic Data Generator — Issue #119")
    print(f"Tables : request_comments, volunteer_rating")
    print(f"Rows   : {n_rows:,} per table")
    print(f"Seed   : {args.seed}\n")

    # ── Load users and requests ──────────────────
    ext_users    = args.users_csv    is not None
    ext_requests = args.requests_csv is not None

    if ext_users and ext_requests:
        print("Mode: EXTERNAL CSVs — using real user_ids and req_ids for full FK integrity")
        users    = load_external_users(args.users_csv)
        requests = load_external_requests(args.requests_csv)
    elif ext_users or ext_requests:
        print("Warning: provide BOTH --users-csv and --requests-csv for full FK integrity.")
        print("Falling back to internal pool for missing source.\n")
        pool = args.pool_size or max(200, n_rows * 3)
        u_int, r_int = build_reference_pool(pool, pool)
        users    = load_external_users(args.users_csv) if ext_users else u_int
        requests = load_external_requests(args.requests_csv) if ext_requests else r_int
    else:
        print("Mode: INTERNAL POOL — generating own user_ids and req_ids")
        pool = args.pool_size or max(200, n_rows * 3)
        print(f"Pool : {pool:,} users × {pool:,} requests\n")
        users, requests = build_reference_pool(pool, pool)

    # ── Generate tables ──────────────────────────
    print("Generating request_comments...")
    comments = generate_request_comments(users, requests, n_rows)
    validate(comments, "request_comments", "comment_id")
    write_csv(comments, "request_comments.csv")

    print("\nGenerating volunteer_rating...")
    ratings = generate_volunteer_rating(users, requests, n_rows)
    validate(ratings, "volunteer_rating", "volunteer_rating_id")
    write_csv(ratings, "volunteer_rating.csv")

    print(f"\nDone. Output: {OUTPUT_DIR}/")
    print(f"  request_comments.csv  — {len(comments):,} rows")
    print(f"  volunteer_rating.csv  — {len(ratings):,} rows")
    print(f"\nOnce users.csv and request.csv are ready from the other team, run:")
    print(f"  python generate_119.py --users-csv database/mock_db/users.csv \\")
    print(f"                         --requests-csv database/mock_db/request.csv")
    print(f"  This regenerates both files with real FK consistency.\n")


if __name__ == "__main__":
    main()
