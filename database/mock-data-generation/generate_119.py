# Day/time fragments injected for temporal realism
_DAYS  = ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"]
_TIMES = ["morning", "afternoon", "evening", "around noon", "at 3 PM", "at 10 AM",
          "after lunch", "first thing in the morning", "by end of day"]


def _synonym_replace(text: str) -> str:
    """Replace 0-2 words/phrases with synonyms (30% chance each)."""
    result = text
    for word, synonyms in SYNONYM_MAP.items():
        if word in result.lower() and random.random() < 0.30:
            replacement = random.choice(synonyms)
            result = re.sub(
                re.escape(word), replacement, result,
                count=1, flags=re.IGNORECASE
            )
    return result


def _inject_typo(text: str) -> str:
    """Swap two adjacent characters (2% chance). Real users make typos."""
    if random.random() < 0.02 and len(text) > 4:
        idx = random.randint(1, len(text) - 3)
        text = text[:idx] + text[idx + 1] + text[idx] + text[idx + 2:]
    return text


def _vary_punctuation(text: str) -> str:
    """Alter ending punctuation (15% chance)."""
    if random.random() < 0.15 and text:
        text = text.rstrip('.!,')
        text += random.choice(['.', '!', '!!', ' :)', '.', '...'])
    return text


def _vary_casing(text: str) -> str:
    """Some users type all lowercase (8% chance)."""
    if random.random() < 0.08:
        text = text.lower()
    return text


def _inject_temporal_detail(text: str) -> str:
    """10% chance to swap generic timing with a specific day/time."""
    if random.random() < 0.10:
        day  = random.choice(_DAYS)
        time = random.choice(_TIMES)
        # Try to replace vague timing markers
        for vague in ["this week", "soon", "shortly", "for the upcoming week"]:
            if vague in text.lower():
                text = re.sub(re.escape(vague), f"on {day} {time}",
                              text, count=1, flags=re.IGNORECASE)
                break
    return text


def perturb(text: str) -> str:
    """Full Tier 2 perturbation pipeline."""
    text = _synonym_replace(text)
    text = _inject_temporal_detail(text)
    text = _inject_typo(text)
    text = _vary_punctuation(text)
    text = _vary_casing(text)
    return text


