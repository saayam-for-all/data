# Weighted to reflect real usage — Food/Healthcare/Education most common
PARENT_CATEGORIES = ["1", "2", "3", "4", "5", "6", "0.0.0.0.0"]
PARENT_WEIGHTS    = [0.22, 0.08, 0.17, 0.18, 0.20, 0.10, 0.05]


def get_parent_cat_id(cat_id: str) -> str:
    """Derive parent cat_id from a materialized path.
    '3.3.7' → '3', '5.1.3' → '5', '2.1' → '2', '1' → '1'
    """
    parts = cat_id.split(".")
    if len(parts) <= 1:
        return cat_id
    return parts[0]


def random_category() -> tuple:
    """Pick a realistic (parent_cat_id, leaf_cat_id) pair.
    Returns official materialized path IDs — ready for req_cat_id.
    """
    parent = random.choices(PARENT_CATEGORIES, weights=PARENT_WEIGHTS, k=1)[0]
    leaf   = random.choice(CATEGORY_TREE[parent])
    return parent, leaf


