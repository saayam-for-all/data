# ─────────────────────────────────────────────
# BUILD INTERNAL POOL (when no external CSVs)
# ─────────────────────────────────────────────

def build_reference_pool(n_users: int, n_requests: int) -> tuple:
    """Generate internal user/request pools when external CSVs not provided."""
    users = [{"user_id": make_user_id()} for _ in range(n_users)]

    requests = []
    for _ in range(n_requests):
        parent, sub  = random_category()
        created_at   = random_timestamp(START_DATE, END_DATE)
        requests.append({
            "req_id":      make_request_id(),
            "category":    parent,
            "subcategory": sub,
            "location":    random.choice(LOCATIONS),
            "created_at":  created_at,
        })

    return users, requests


# ─────────────────────────────────────────────
# TABLE 1: request_comments
# ─────────────────────────────────────────────

def generate_request_comments(users: list, requests: list, n_rows: int) -> list:
    """
    Generate synthetic request_comments rows.
    comment_desc uses real Saayam category-specific language.
    """
    rows = []
    for comment_id in range(1, n_rows + 1):
        req = random.choice(requests)

        # 25% chance the commenter is the requester (follow-up)
        # 75% chance it's a volunteer (different user)
        if random.random() < 0.25:
            req_user = next((u for u in users
                             if u["user_id"] == req.get("req_user_id", "")),
                            random.choice(users))
            commenter = req_user
        else:
            commenter = random.choice(users)

        req_dt        = datetime.strptime(req["created_at"], "%Y-%m-%d %H:%M:%S")
        comment_start = req_dt
        comment_end   = min(req_dt + timedelta(days=30), END_DATE)
        created_at    = random_timestamp(comment_start, comment_end)

        created_dt   = datetime.strptime(created_at, "%Y-%m-%d %H:%M:%S")
        updated_end  = min(created_dt + timedelta(hours=72), END_DATE)
        last_updated = random_timestamp(created_dt, updated_end)

        comment_desc = pick_comment(
            req.get("category", "General"),
            req.get("subcategory", "General")
        )

        rows.append({
            "comment_id":      comment_id,
            "req_id":          req["req_id"],
            "commenter_id":    commenter["user_id"],
            "comment_desc":    comment_desc,
            "created_at":      created_at,
            "last_updated_at": last_updated,
            "isdeleted":       "TRUE" if random.random() < 0.05 else "FALSE",
        })

    return rows


# ─────────────────────────────────────────────
# TABLE 2: volunteer_rating
# ─────────────────────────────────────────────

def generate_volunteer_rating(users: list, requests: list, n_rows: int) -> list:
    """
    Generate synthetic volunteer_rating rows.
    Rating distribution is realistic (skewed positive).
    Feedback text matches rating sentiment.
    One unique (user_id, request_id) pair per row — no duplicates.
    """
    rows       = []
    seen_pairs = set()
    rating_id  = 1
    attempts   = 0
    max_att    = n_rows * 10

    while len(rows) < n_rows and attempts < max_att:
        attempts += 1
        req  = random.choice(requests)
        user = random.choice(users)
        pair = (user["user_id"], req["req_id"])
        if pair in seen_pairs:
            continue
        seen_pairs.add(pair)

        rating   = random.choices([1, 2, 3, 4, 5], weights=RATING_WEIGHTS, k=1)[0]
        feedback = random.choice(FEEDBACK[rating])

        req_dt       = datetime.strptime(req["created_at"], "%Y-%m-%d %H:%M:%S")
        rating_start = req_dt + timedelta(hours=1)
        rating_end   = min(req_dt + timedelta(days=14), END_DATE)
        if rating_start >= rating_end:
            last_update = req_dt.strftime("%Y-%m-%d %H:%M:%S")
        else:
            last_update = random_timestamp(rating_start, rating_end)

        rows.append({
            "volunteer_rating_id": rating_id,
            "user_id":             user["user_id"],
            "request_id":          req["req_id"],
            "rating":              rating,
            "feedback":            feedback,
            "last_update_date":    last_update,
        })
        rating_id += 1

    if len(rows) < n_rows:
        print(f"  Warning: only {len(rows)} unique pairs possible. "
              f"Increase pool size with --pool-size.")
    return rows


# ─────────────────────────────────────────────
# VALIDATION
# ─────────────────────────────────────────────

def validate(rows: list, table_name: str, pk_col: str):
    print(f"\n  Validation — {table_name}")
    print(f"     Rows generated : {len(rows):,}")
    pk_vals = [r[pk_col] for r in rows]
    dupes   = len(pk_vals) - len(set(pk_vals))
    nulls   = sum(1 for v in pk_vals if not v)
    print(f"     PK duplicates  : {dupes}  {'OK' if dupes == 0 else 'FAIL'}")
    print(f"     Null PKs       : {nulls}  {'OK' if nulls == 0 else 'FAIL'}")
    if table_name == "volunteer_rating":
        from collections import Counter
        dist  = Counter(r["rating"] for r in rows)
        pairs = [(r["user_id"], r["request_id"]) for r in rows]
        dp    = len(pairs) - len(set(pairs))
        print(f"     Rating dist    : {dict(sorted(dist.items()))}")
        print(f"     Dupe pairs     : {dp}  {'OK' if dp == 0 else 'FAIL'}")


# ─────────────────────────────────────────────
# CSV WRITER
# ─────────────────────────────────────────────

def write_csv(rows: list, filename: str):
    if not rows:
        return
    path = OUTPUT_DIR / filename
    with open(path, "w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=rows[0].keys())
        writer.writeheader()
        writer.writerows(rows)
    print(f"  Saved {filename}: {len(rows):,} rows → {path}")


