def load_external_users(filepath: str) -> list:
    """
    Load user_ids from the users.csv generated by another team.
    Expects a CSV with at minimum a 'user_id' column.
    """
    users = []
    with open(filepath, newline='', encoding='utf-8') as f:
        for row in csv.DictReader(f):
            users.append({"user_id": row["user_id"]})
    print(f"  Loaded {len(users):,} users from {filepath}")
    return users


def load_external_requests(filepath: str) -> list:
    """
    Load request data from request.csv generated by another team.
    Expects columns: req_id, req_cat_id (or req_cat), req_loc, created_at (or req_created_at)
    Column names are flexible — we try common variants.
    """
    requests = []
    with open(filepath, newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        cols = reader.fieldnames or []

        # Detect column name variants
        id_col  = next((c for c in cols if 'req_id' in c.lower()), None)
        cat_col = next((c for c in cols if 'cat' in c.lower()), None)
        loc_col = next((c for c in cols if 'loc' in c.lower()), None)
        dt_col  = next((c for c in cols if 'creat' in c.lower() or 'date' in c.lower()), None)

        for row in reader:
            # Parse created_at — fall back to a random date if missing/malformed
            raw_dt = row.get(dt_col, "") if dt_col else ""
            try:
                created_at = datetime.strptime(raw_dt[:19], "%Y-%m-%d %H:%M:%S")
            except Exception:
                created_at = START_DATE + timedelta(
                    days=random.randint(0, (END_DATE - START_DATE).days)
                )

            # Map category to our parent hierarchy
            raw_cat = row.get(cat_col, "General") if cat_col else "General"
            parent  = map_category_to_parent(raw_cat)
            sub     = raw_cat if raw_cat in [s for subs in CATEGORY_TREE.values()
                                              for s in subs] else parent

            requests.append({
                "req_id":    row.get(id_col, make_request_id()) if id_col else make_request_id(),
                "category":  parent,
                "subcategory": sub,
                "location":  row.get(loc_col, random.choice(LOCATIONS)) if loc_col else random.choice(LOCATIONS),
                "created_at": created_at.strftime("%Y-%m-%d %H:%M:%S"),
            })

    print(f"  Loaded {len(requests):,} requests from {filepath}")
    return requests


