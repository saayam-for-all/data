# TIER 3 — DIVERSITY VALIDATOR
# Ensures generated corpus is ML-training-grade:
#   - Unique string ratio (target ≥ 85%)
#   - No single 4-gram dominates (target: max 1% frequency)
#   - Reports stats so we know exactly where we stand
# ═══════════════════════════════════════════════════════════════

def validate_text_diversity(texts: list, label: str,
                            min_unique_ratio: float = 0.80,
                            max_ngram_freq: float = 0.02) -> None:
    """Print diversity diagnostics for a generated text corpus."""
    from collections import Counter

    n = len(texts)
    unique = len(set(texts))
    ratio  = unique / n if n > 0 else 0

    # 4-gram frequency analysis
    ngram_counts = Counter()
    for t in texts:
        words = t.lower().split()
        for i in range(len(words) - 3):
            ngram_counts[tuple(words[i:i + 4])] += 1

    max_freq      = max(ngram_counts.values()) / n if ngram_counts else 0
    top_ngram     = ngram_counts.most_common(1)[0] if ngram_counts else (("—",), 0)
    total_unique  = len(ngram_counts)

    print(f"\n  Diversity — {label}")
    print(f"     Total texts        : {n:,}")
    print(f"     Unique texts       : {unique:,}  ({100 * ratio:.1f}%)"
          f"  {'OK' if ratio >= min_unique_ratio else 'LOW — consider more grammar slots'}")
    print(f"     Unique 4-grams     : {total_unique:,}")
    print(f"     Max 4-gram freq    : {100 * max_freq:.2f}%"
          f"  {'OK' if max_freq <= max_ngram_freq else 'HIGH — most repeated:'}")
    if max_freq > max_ngram_freq:
        print(f"       Most common      : {' '.join(top_ngram[0])} ({top_ngram[1]:,}×)")

